document.querySelector("seleciona pelo ID, pela CLASS ou pelo nome da TAG no HTML")

// querySelectorAll() (Nodelist)
document.querySelectorAll("seleciona todos os seletores de acordo com o ID, CLASS ou nome da TAG no HTML")